def get_max_num(numbers):
    return max(numbers)

numbers_list = [5, 2, 8, 1, 6]
max_number = get_max_num(numbers_list)
print(max_number)
print("-----------------------------------")
def get_min_num(numbers):
    return min(numbers)

numbers_list = [5, 2, 8, 1, 6]
min_number = get_min_num(numbers_list)
print(min_number)

